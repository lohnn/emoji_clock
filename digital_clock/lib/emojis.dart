extension MyListStuff<T> on List<T> {
  List<T> shuffle() {
    var random = new Random();

    // Go through all elements.
    for (var i = this.length - 1; i > 0; i--) {
      // Pick a pseudorandom number according to the list length
      var n = random.nextInt(i + 1);

      var temp = this[i];
      this[i] = this[n];
      this[n] = temp;
    }

    return this;
  }
}
